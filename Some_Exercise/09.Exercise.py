# Class Name: Addition
# Method:
# Function Name: add
# Parameters: a (int), b (int)
# Return Type: int
# Static: Yes (Use the @staticmethod decorator)
# Task: Returns the sum of the values given as parameters.
# Examples:

# Input: a = 3, b = 4
# Output: 7

class Addition: 
    @staticmethod
    def add(a, b):
        return a + b
    

    